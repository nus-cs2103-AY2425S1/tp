package seedu.address.model.person;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertFalse;
import static org.junit.jupiter.api.Assertions.assertNotEquals;
import static org.junit.jupiter.api.Assertions.assertNull;
import static org.junit.jupiter.api.Assertions.assertTrue;
import static seedu.address.logic.commands.CommandTestUtil.VALID_COURSE_BOB;
import static seedu.address.logic.commands.CommandTestUtil.VALID_EMAIL_BOB;
import static seedu.address.logic.commands.CommandTestUtil.VALID_NAME_BOB;
import static seedu.address.logic.commands.CommandTestUtil.VALID_PHONE_BOB;
import static seedu.address.logic.commands.CommandTestUtil.VALID_TAG_STRUGGLING;
import static seedu.address.testutil.Assert.assertThrows;
import static seedu.address.testutil.TypicalPersons.ALICE;
import static seedu.address.testutil.TypicalPersons.BOB;

import org.junit.jupiter.api.Test;

import seedu.address.testutil.PersonBuilder;

public class PersonTest {

    @Test
    public void asObservableList_modifyList_throwsUnsupportedOperationException() {
        Person person = new PersonBuilder().build();
        assertThrows(UnsupportedOperationException.class, () -> person.getTags().remove(0));
    }

    @Test
    public void isSamePerson() {
        // same object -> returns true
        assertTrue(ALICE.isSamePerson(ALICE));

        // null -> returns false
        assertFalse(ALICE.isSamePerson(null));

        // same name, all other attributes different -> returns false
        Person editedAlice = new PersonBuilder(ALICE).withPhone(VALID_PHONE_BOB).withEmail(VALID_EMAIL_BOB)
                .withCourse(VALID_COURSE_BOB).withTags(VALID_TAG_STRUGGLING).build();
        assertFalse(ALICE.isSamePerson(editedAlice));

        // different name, all other attributes same -> returns true
        editedAlice = new PersonBuilder(ALICE).withName(VALID_NAME_BOB).build();
        assertTrue(ALICE.isSamePerson(editedAlice));

        // email differs in case, all other attributes same -> returns true
        Person editedBob = new PersonBuilder(BOB).withEmail(VALID_EMAIL_BOB.toUpperCase()).build();
        assertTrue(BOB.isSamePerson(editedBob));

        // course differs in case, all other attributes same -> returns true
        editedBob = new PersonBuilder(BOB).withCourse(VALID_COURSE_BOB.toLowerCase()).build();
        assertTrue(BOB.isSamePerson(editedBob));

        // email has trailing spaces, all other attributes same -> returns true
        String emailWithTrailingSpace = VALID_EMAIL_BOB;
        editedBob = new PersonBuilder(BOB).withEmail(emailWithTrailingSpace).build();
        assertTrue(BOB.isSamePerson(editedBob));

        // course has trailing spaces, all other attributes same -> returns true
        String courseWithTrailingSpace = VALID_COURSE_BOB;
        editedBob = new PersonBuilder(BOB).withCourse(courseWithTrailingSpace).build();
        assertTrue(BOB.isSamePerson(editedBob));
    }

    @Test
    public void equals() {
        // same values -> returns true
        Person aliceCopy = new PersonBuilder(ALICE).build();
        assertTrue(ALICE.equals(aliceCopy));

        // same object -> returns true
        assertTrue(ALICE.equals(ALICE));

        // null -> returns false
        assertFalse(ALICE.equals(null));

        // different type -> returns false
        assertFalse(ALICE.equals(5));

        // different person -> returns false
        assertFalse(ALICE.equals(BOB));

        // different name -> returns false
        Person editedAlice = new PersonBuilder(ALICE).withName(VALID_NAME_BOB).build();
        assertFalse(ALICE.equals(editedAlice));

        // different phone -> returns false
        editedAlice = new PersonBuilder(ALICE).withPhone(VALID_PHONE_BOB).build();
        assertFalse(ALICE.equals(editedAlice));

        // different email -> returns false
        editedAlice = new PersonBuilder(ALICE).withEmail(VALID_EMAIL_BOB).build();
        assertFalse(ALICE.equals(editedAlice));

        // different course -> returns false
        editedAlice = new PersonBuilder(ALICE).withCourse(VALID_COURSE_BOB).build();
        assertFalse(ALICE.equals(editedAlice));

        // different tags -> returns false
        editedAlice = new PersonBuilder(ALICE).withTags(VALID_TAG_STRUGGLING).build();
        assertFalse(ALICE.equals(editedAlice));
    }

    @Test
    public void addGrade_immutabilityCheck() {
        // Create initial person and make a copy
        Person originalPerson = new PersonBuilder(ALICE).build();
        Person copyOfOriginal = new PersonBuilder(ALICE).build();

        // Add a grade to the original person
        Grade newGrade = new Grade("Midterm", 85.0F, 30.0F);
        Person updatedPerson = originalPerson.addGrade(newGrade);

        // Check that the original person remains unchanged
        assertEquals(copyOfOriginal, originalPerson,
                "Original person should remain unchanged after adding a grade to the updated person");

        // Verify that the updated person is different from the original
        assertNotEquals(updatedPerson, originalPerson,
                "Updated person should differ from the original person after adding a grade");

        // Verify the new grade is present in the updated person but not in the original
        assertEquals(newGrade, updatedPerson.getGradeList().getGrade(newGrade.getTestName().toLowerCase()),
                "Updated person should contain the newly added grade");
        assertNull(originalPerson.getGradeList().getGrade(newGrade.getTestName()),
                "Original person should not contain the newly added grade");
    }


    @Test
    public void toStringMethod() {
        String expected = Person.class.getCanonicalName() + "{name=" + ALICE.getName() + ", phone=" + ALICE.getPhone()
                + ", email=" + ALICE.getEmail() + ", course=" + ALICE.getCourse() + ", tags=" + ALICE.getTags()
                + ", gradeList=" + ALICE.getGradeList() + ", attendanceList=" + ALICE.getAttendanceList() + "}";
        assertEquals(expected, ALICE.toString());
    }
}
