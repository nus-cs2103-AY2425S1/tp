package seedu.address.logic.parser;

import static seedu.address.logic.Messages.MESSAGE_INVALID_PERSON_DISPLAYED_INDEX_FORMAT;
import static seedu.address.logic.parser.CommandParserTestUtil.assertParseFailure;
import static seedu.address.logic.parser.CommandParserTestUtil.assertParseSuccess;
import static seedu.address.testutil.TypicalIndexes.INDEX_FIRST_PERSON;

import org.junit.jupiter.api.Test;

import seedu.address.logic.commands.InfoCommand;

public class InfoCommandParserTest {

    private InfoCommandParser parser = new InfoCommandParser();

    @Test
    public void parse_validArgs_returnsInfoCommand() {
        assertParseSuccess(parser, "1", new InfoCommand(INDEX_FIRST_PERSON));
    }

    @Test
    public void parse_invalidArgs_throwsParseException() {
        assertParseFailure(parser, "a", String.format(MESSAGE_INVALID_PERSON_DISPLAYED_INDEX_FORMAT,
                InfoCommand.MESSAGE_USAGE));
    }
}
