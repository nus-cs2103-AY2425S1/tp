package seedu.ddd.model.contact.common;

/**
 * Represents the type of contact in the system.
 * <p>
 * This enum is used to distinguish between different types of contacts in the application,
 * such as vendors and clients.
 * </p>
 */
public enum ContactType {
    /**
     * Represents a vendor contact.
     */
    VENDOR,

    /**
     * Represents a client contact.
     */
    CLIENT
}
