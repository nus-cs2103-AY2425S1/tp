package seedu.address.logic;

import java.util.Set;
import java.util.stream.Collectors;
import java.util.stream.Stream;

import seedu.address.logic.parser.Prefix;
import seedu.address.model.person.Person;
import seedu.address.model.wedding.Wedding;

/**
 * Container for user visible messages.
 */
public class Messages {

    public static final String MESSAGE_UNKNOWN_COMMAND = "Unknown command";
    public static final String MESSAGE_INVALID_COMMAND_FORMAT = "Invalid command format! \n%1$s";
    public static final String MESSAGE_INVALID_TAG_LIST_SIZE = "A contact can only have up to 6 tags";
    public static final String MESSAGE_INVALID_PERSON_DISPLAYED_INDEX =
            "1 or more contact indexes provided is invalid";

    public static final String MESSAGE_INVALID_NEGATIVE_INDEX = "Contact index must be a positive number.";

    public static final String MESSAGE_TRY_PERSON_LIST_MODE =
            "You may enter the 'list' command and try assigning the person to this wedding again.";

    public static final String MESSAGE_NO_CONTACTS_SPECIFIED = "No contacts specified!";
    public static final String MESSAGE_INVALID_WEDDING_DISPLAYED_INDEX = "The wedding index provided is invalid";
    public static final String MESSAGE_PERSONS_LISTED_OVERVIEW = "%1$d persons listed!";
    public static final String MESSAGE_DUPLICATE_FIELDS =
                "Multiple values specified for the following single-valued field(s): ";

    /**
     * Returns an error message indicating the duplicate prefixes.
     */
    public static String getErrorMessageForDuplicatePrefixes(Prefix... duplicatePrefixes) {
        assert duplicatePrefixes.length > 0;

        Set<String> duplicateFields =
                Stream.of(duplicatePrefixes).map(Prefix::toString).collect(Collectors.toSet());

        return MESSAGE_DUPLICATE_FIELDS + String.join(" ", duplicateFields);
    }

    /**
     * Formats the {@code person} for display to the user.
     */
    public static String format(Person person) {
        final StringBuilder builder = new StringBuilder();
        builder.append(person.getName())
                .append("; Phone: ")
                .append(person.getPhone())
                .append("; Email: ")
                .append(person.getEmail())
                .append("; Address: ")
                .append(person.getAddress())
                .append("; Tags: ");
        person.getTags().forEach(builder::append);
        return builder.toString();
    }

    /**
     * Formats the {@code wedding} for display to the user.
     */
    public static String formatWedding(Wedding wedding) {
        final StringBuilder builder = new StringBuilder();
        builder.append(wedding.getWeddingName())
                .append("; Date: ")
                .append(wedding.getWeddingDate());
        return builder.toString();
    }

}
