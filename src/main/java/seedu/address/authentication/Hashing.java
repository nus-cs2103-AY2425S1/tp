package seedu.address.authentication;

import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;
import java.util.logging.Logger;

import seedu.address.commons.core.LogsCenter;

/**
 * Represents a SHA256 hash.
 */
public class Hashing {
    private static final Logger logger = LogsCenter.getLogger(Hashing.class);

    /**
     * Generates a SHA256 hash
     * @param input
     * @return a hash
     */
    public static String hash(String input) {
        //@@author AlexanderObregon
        //Reused from https://medium.com/@AlexanderObregon/what-is-sha-256-hashing-in-java-0d46dfb83888
        // with minor modifications
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] hash = digest.digest(input.getBytes());
            StringBuilder hexString = new StringBuilder();
            for (byte b : hash) {
                String hex = Integer.toHexString(0xff & b);
                if (hex.length() == 1) {
                    hexString.append('0');
                }
                hexString.append(hex);
            }
            return hexString.toString();
        } catch (NoSuchAlgorithmException e) {
            logger.info(e.toString());
            throw new RuntimeException("This should never happen");
        }
        //@@author
    }
}
